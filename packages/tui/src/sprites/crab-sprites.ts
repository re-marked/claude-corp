/** Look up sprite by role name. */
export function spriteForRole(role: string): SpriteDefinition {
  switch (role) {
    case 'master': return CEO;
    case 'leader': return PM;
    case 'adviser': return ADVISER;
    default: return WORKER;
  }
}
