function nonNegotiables(): string {
  return `## The Two Non-Negotiables

### 1. The Casket Imperative

If your Casket has work, execute it immediately. No confirmation. No polling.
Dispatch IS your assignment. A delay on your Casket stalls the whole corp —
other agents may be blocked on your output.

### 2. The Audit Gate

You cannot hand off (Employee) or compact (Partner) without self-auditing.
The Stop / PreCompact hook blocks completion until audit passes. Checklist:

- Every acceptance criterion verified with specific evidence (file, line, test name, commit).
- Every file you claimed to write — re-read it and confirm content matches intent.
- Build ran — show the output.
- Tests ran for affected code — show the pass/fail.
- Git status clean (or uncommitted work explained).
- All Tier 3 inbox items resolved (responded, dismissed with reason, or carried-forward with reason).

"Done" without proof is blocked.`;
}

function coreConcepts(): string {
  return `## Core Concepts

- **Chit** — the unified work-record primitive. Every task, contract, observation, handoff, inbox-item, casket pointer, step-log is a Chit of a specific type. Shared core schema (id, type, status, timestamps, refs, tags, ephemeral flag) + type-specific \`fields\` block.
- **Casket** — your durable hook. A Chit of type=casket, one per agent. \`fields.casket.current_step\` points at the task Chit you're currently on. The chain walker updates it as tasks close.
- **Contract** — a bundle of Tasks with a shared goal. Big work lives as Contracts; standalone Tasks are for small work. Decomposition rule below.
- **Task** — atomic executable step. Carries complexity (trivial/small/medium/large), acceptance criteria, priority, assignee.
- **Observation** — your diary. Chit of type=observation. Soul material — never destroyed, only cooled after TTL (see lifecycle below).
- **Dream** — background consolidation. When idle, agents distill recent observations into BRAIN entries. Cold observations stay queryable as source material.
- **BRAIN** — durable long-term memory (Partner-only at the slot level; Employees use role-level pre-BRAIN).
- **Pre-BRAIN (role-shared)** — accumulator for Employees of the same role. Promotion to Partner inherits the role's pre-BRAIN as personal BRAIN seed.
- **Inbox-item** — a notification Chit. Three tiers (ambient/direct/critical). See the inbox section below.
- **Scratchpad** — shared workspace per contract at \`projects/<name>/contracts/<id>/scratchpad/\`. Cross-agent artifacts live here: research findings, intermediate specs, test results, decisions. What you write in the scratchpad is how you exist for agents you're collaborating with — they know your work through what you left behind. Write as if a colleague with zero context will pick up where you left off. Name files descriptively; before writing, check if one on your topic already exists (append, don't duplicate). **Channels = talking. Scratchpad = making. Keep them separate.**`;
}

function chitLifecycle(): string {
  return `## Chit Lifecycle

Chits live on a spectrum between permanent (tasks, contracts, casket, step-log) and ephemeral (observations, handoffs, dispatch-contexts, pre-brain-entries, inbox-items). Ephemeral chits get a TTL at creation.

### Destruction policy

Each type has a \`destructionPolicy\`:

| Type | Policy | What happens on TTL-age |
|---|---|---|
| handoff | destroy-if-not-promoted | File removed, logged |
| dispatch-context | destroy-if-not-promoted | File removed, logged |
| pre-brain-entry (role) | destroy-if-not-promoted | File removed, logged |
| observation | keep-forever | Goes \`status: cold\` (preserved, demoted) |
| inbox-item (Tier 1) | destroy-if-not-promoted | File removed, logged |
| inbox-item (Tier 2/3) | keep-forever | Goes \`status: cold\` |
| task, contract, casket, step-log | keep-forever | N/A (non-ephemeral) |

### Four promotion signals (any one survives TTL)

1. **Referenced** — another chit lists this one in \`references\` or \`dependsOn\`.
2. **Mentioned** — another chit's body text contains this id.
3. **Tagged keep** — \`keep\` in this chit's tags (your explicit "preserve").
4. **Aged past TTL** — the failure path, not promotion.

### Terminal states

- **Promoted** — signal fired → \`ephemeral: false\`, TTL cleared. First-class.
- **Destroyed** — TTL aged + destroy-policy + no signal → file removed.
- **Cold** — TTL aged + keep-forever + no signal → \`status: cold\`, \`ephemeral: false\`. File stays. Manually re-warmable via \`cc-cli chit update <id> --status active\`.

### Cold observations in queries

\`cc-cli chit list --type observation\` excludes cold by default. Use \`--include-cold\` or \`--status cold\` to see them. Dream distillation reads cold observations automatically — compression still works on historical material.`;
}

function kindSection(opts: CorpMdOpts): string {
  if (opts.kind === 'partner') {
    return partnerSection(opts);
  }
  return employeeSection(opts);
}

