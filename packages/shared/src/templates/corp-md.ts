/**
 * CORP.md template — the orchestration manual injected at every session
 * start via \`cc-cli wtf\`.
 *
 * This is the "static reference" layer of Project 0.7's dynamic system-
 * prompt architecture. It collects EVERYTHING an agent needs to know
 * about how the corp works — architecture, roles, lifecycle, commands,
 * red lines — into one flat document. Generated fresh by each wtf call
 * (never hand-maintained), gitignored in the agent's workspace.
 *
 * The spec lives in REFACTOR.md section 0.7. The prose here should
 * track that spec closely; when the spec changes, this template is the
 * artifact that needs to update.
 *
 * Pure function, no I/O. The wtf command prepends a timestamp header +
 * the situational layer (current task, inbox, handoff) when emitting
 * to stdout or writing to disk. Keeping this pure means the same
 * function serves both the Claude Code harness (via \`cc-cli wtf\`) and
 * the OpenClaw daemon fragment, so both substrates see identical text.
 */

export type CorpMdKind = 'partner' | 'employee';

export interface CorpMdOpts {
  /** Partner (persistent identity) vs Employee (ephemeral slot). Drives which kind-specific sections appear. */
  kind: CorpMdKind;
  /** Agent slug (members.json id). Used in example commands. */
  agentSlug: string;
  /** Human-readable name. For Partners this is the founder-given name; for Employees their self-chosen slot name. */
  displayName: string;
  /** Role — e.g. 'CEO', 'Backend Engineer', 'Herald'. Displayed in the heading. */
  role: string;
  /** Corp name — e.g. 'my-corporation'. */
  corpName: string;
  /** Absolute workspace path for this agent. Used in file-path examples. */
  workspacePath: string;
  /** Employee-only: path to role-level pre-BRAIN (for the "Your Role's Pre-BRAIN" section). */
  rolePreBrainPath?: string;
}

/** Main entry. Composes all sections in canonical order. */
export function buildCorpMd(opts: CorpMdOpts): string {
  const sections = [
    heading(opts),
    intro(),
    architecture(opts),
    roles(),
    nonNegotiables(),
    coreConcepts(),
    chitLifecycle(),
    kindSection(opts),
    taskComplexity(),
    sessionModel(opts),
    commandReference(),
    communication(),
    auditGate(),
    writeDownWhatMatters(),
    brainSection(),
    coordinatingContracts(),
    executingATask(),
    filePaths(opts),
    commonPatterns(),
    redLines(),
    commonMistakes(),
  ];
  return sections.join('\n\n') + '\n';
}

