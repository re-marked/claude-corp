function heading(opts: CorpMdOpts): string {
  return `# ${opts.corpName} — Orchestration Manual`;
}

function intro(): string {
  return [
    `This file contains everything you need to know to work in this corp.`,
    `Generated by \`cc-cli wtf\`. Don't hand-edit — regenerate instead.`,
  ].join('\n');
}

